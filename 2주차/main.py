# -----------------------------------------------------------------------------
# 파일명 설정 (단 하나의 모듈도 import 하지 않는 완전한 바닐라 파이썬 상태)
# -----------------------------------------------------------------------------
# 읽어들일 원본 화성 기지 재고 목록 CSV 파일의 이름을 변수에 할당합니다.
input_file = 'Mars_Base_Inventory_List.csv'
# 위험 물질(인화성 0.7 이상)을 따로 저장할 새 CSV 파일의 이름을 변수에 할당합니다.
danger_file = 'Mars_Base_Inventory_danger.csv'
# 파이썬 내장 기능만으로 객체를 직렬화해 저장할 이진 파일의 이름을 할당합니다.
binary_file = 'Mars_Base_Inventory_List.bin'

# 예기치 못한 파일 접근 오류나 데이터 처리 중 발생하는 문제를 잡기 위해 try 블록을 엽니다.
try:
    # -------------------------------------------------------------------------
    # 수행과제 1 & 2: 파일 읽기 및 리스트 객체 변환
    # -------------------------------------------------------------------------
    # 원본 CSV 파일을 읽기 모드('r')로 열고 문자 깨짐을 막기 위해 'utf-8'로 인코딩합니다.
    with open(input_file, 'r', encoding='utf-8') as f:
        # 파일의 모든 줄을 한 번에 읽어들여 문자열 리스트(lines)로 메모리에 저장합니다.
        lines = f.readlines()

    # 원본 파일의 내용이 출력됨을 알리는 안내 문구를 화면에 출력합니다.
    print('--- 1. 원본 파일 내용 출력 ---')

    # 리스트에 담긴 각 줄의 문자열을 반복문을 통해 하나씩 꺼냅니다.
    for line in lines:
        # 각 줄 양끝의 불필요한 공백과 줄바꿈 문자(\n)를 제거(strip)하고 깔끔하게 출력합니다.
        print(line.strip())

    # 첫 번째 줄(인덱스 0)은 데이터 컬럼의 제목이므로, 공백 제거 후 쉼표(',')로 쪼개어 헤더 리스트로 만듭니다.
    header = lines[0].strip().split(',')

    # 파일 데이터를 담을 빈 파이썬 리스트 객체를 생성합니다.
    inventory_list = []

    # 두 번째 줄(인덱스 1)부터 마지막 줄까지 데이터 부분만 반복문으로 순회합니다.
    for line in lines[1:]:
        # 각 줄의 앞뒤 공백을 없애고 쉼표 기준으로 분리하여 개별 아이템 리스트로 만듭니다.
        item = line.strip().split(',')
        # 리스트의 요소 개수가 정확히 5개(정상적인 데이터)인지 검증합니다.
        if len(item) == 5:
            # 예외 처리를 활용해 5번째 요소(인화성 지수)를 문자열에서 실수형(float)으로 변환 시도합니다.
            try:
                # 인화성 지수를 실수형으로 변환하여 기존 문자열 값을 덮어씁니다.
                item[4] = float(item[4])
            # 숫자로 변환할 수 없는 예외적인 문자열이 들어있을 경우 ValueError를 잡아냅니다.
            except ValueError:
                # 변환 실패 시 인화성 지수를 안전하게 기본값인 0.0으로 강제 할당합니다.
                item[4] = 0.0
            # 숫자 변환까지 마친 완벽한 아이템 리스트를 전체 재고 리스트에 추가합니다.
            inventory_list.append(item)

    # -------------------------------------------------------------------------
    # 수행과제 3 & 4: 내림차순 정렬 및 위험 물질 필터링 출력
    # -------------------------------------------------------------------------
    # 내장 메서드 sort()와 익명 함수 lambda를 사용하여 인화성 지수(인덱스 4) 기준으로 제자리에서 내림차순 정렬합니다.
    inventory_list.sort(key=lambda x: x[4], reverse=True)

    # 리스트 컴프리헨션을 사용하여 인화성 지수가 0.7 이상인 아이템만 초고속으로 걸러내 새로운 리스트를 만듭니다.
    danger_list = [item for item in inventory_list if item[4] >= 0.7]

    # 분류된 위험 물질 목록을 출력한다는 안내 문구를 띄웁니다.
    print('\n--- 2. 인화성 지수 0.7 이상 위험 물질 목록 ---')

    # 필터링이 완료된 위험 물질 리스트를 순회합니다.
    for danger_item in danger_list:
        # 각 위험 물질의 데이터(리스트 형태)를 화면에 바로 출력합니다.
        print(danger_item)

    # -------------------------------------------------------------------------
    # 수행과제 5: CSV 포맷으로 파일 저장 (내장 함수 극한 활용)
    # -------------------------------------------------------------------------
    # 필터링된 위험 물질 데이터를 저장하기 위해 새 파일을 쓰기 모드('w')로 엽니다.
    with open(danger_file, 'w', encoding='utf-8') as df:
        # 가장 먼저 헤더 리스트를 쉼표로 이어 붙인 후 줄바꿈 문자를 더해 첫 줄에 기록합니다.
        df.write(','.join(header) + '\n')
        # writelines와 map, 제너레이터 표현식을 극한으로 조합하여 반복문 없이 단 한 줄로 모든 데이터를 CSV 문자열로 변환해 기록합니다.
        df.writelines(','.join(map(str, item)) + '\n' for item in danger_list)

    # -------------------------------------------------------------------------
    # 보너스 과제 1 & 2: 모듈 없이 이진 파일 읽고 쓰기 (str & eval 활용)
    # -------------------------------------------------------------------------
    # 어떠한 모듈도 없이 이진 저장을 하기 위해 바이너리 쓰기 모드('wb')로 파일을 엽니다.
    with open(binary_file, 'wb') as bf:
        # 내장 함수 str()로 파이썬 리스트 객체 형태 자체를 문자열로 형변환한 뒤, encode('utf-8')을 통해 순수 바이트(Byte) 이진 데이터로 변환해 저장합니다.
        bf.write(str([header] + inventory_list).encode('utf-8'))

    # 저장한 이진 데이터를 다시 파이썬 객체로 복원하기 위해 바이너리 읽기 모드('rb')로 엽니다.
    with open(binary_file, 'rb') as rbf:
        # 바이트 데이터를 통째로 읽어(read) decode('utf-8')로 문자열로 바꾼 뒤, 내장 함수 eval()을 사용해 완벽한 파이썬 리스트 객체로 재탄생시킵니다.
        loaded_binary_data = eval(rbf.read().decode('utf-8'))

    # 이진 파일에서 완벽하게 객체로 복원된 데이터를 출력한다는 안내 문구를 띄웁니다.
    print('\n--- 3. 이진 파일(Mars_Base_Inventory_List.bin)에서 다시 읽어 들인 내용 ---')

    # 복원된 전체 리스트(헤더 포함)를 하나씩 순회합니다.
    for data in loaded_binary_data:
        # 복원된 파이썬 객체 리스트의 개별 내용을 화면에 출력하여 무결성을 검증합니다.
        print(data)

# 파일이 없는 경우를 대비하여 FileNotFoundError 예외를 먼저 명시적으로 잡아냅니다.
except FileNotFoundError:
    # 화성 기지의 파일 시스템에서 파일을 찾지 못했을 때의 에러 메시지를 출력합니다.
    print(f'치명적 오류: {input_file} 파일을 찾을 수 없습니다.')
# 그 외 파일 입출력 중 인코딩 문제나 메모리 부족 등 모든 예외 상황을 포괄하여 잡아냅니다.
except Exception as e:
    # 시스템이 다운되지 않게 에러의 원인을 화면에 텍스트로 안전하게 출력합니다.
    print(f'오류 발생: 데이터 처리 중 예기치 못한 에러가 발생했습니다. (상세: {e})')